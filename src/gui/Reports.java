    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            java.util.Date dt1=d1.getDate();
            java.util.Date dt2=d2.getDate();
            //convert java.util.Date to java.sql.Date
            java.sql.Date date1=new java.sql.Date(dt1.getTime());
            java.sql.Date date2=new java.sql.Date(dt2.getTime());
            ResultSet rs=db.DbConnect.st.executeQuery("select * from sell where sell_date >= '"+date1+"' and sell_date <= '"+date2+"'");
            int profit=0;
            int sAmt=0;
            int cAmt=0;
            while(rs.next()){
                sAmt += rs.getInt("sell_amount");
                cAmt += rs.getInt("cost_amount");
            }
            profit=sAmt-cAmt;
            p.setText(profit+"");
            s.setText(sAmt+"");
            pr.setText(cAmt+"");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

